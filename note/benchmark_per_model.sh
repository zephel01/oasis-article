#!/bin/bash

# ============================================================================
# モデル別ベンチマーク実行スクリプト
# 各モデルごとに OS を再起動して独立実行
# ============================================================================

BENCHMARK_DIR="$HOME/llm/benchmark"
DEVICE="Jetson Orin Nano Super (8GB)"

echo "============================================================"
echo "  モデル別ベンチマーク実行（再起動による独立実行）"
echo "============================================================"
echo ""
echo "このスクリプトは以下を実行します："
echo "  1. Bonsai-1.7B をテスト → OS 再起動"
echo "  2. LFM2.5 をテスト → OS 再起動"
echo "  3. Gemma 4 E2B をテスト"
echo ""
echo "各モデル実行後に自動的に再起動されます。"
echo "実行には 30-60 分かかります。"
echo ""
read -p "開始してもよろしいですか？ (y/n): " -n 1 -r
echo ""

if [[ ! $REPLY =~ ^[Yy]$ ]]; then
    echo "キャンセルしました。"
    exit 0
fi

# ============================================================================
# ステップ 0: 初期クリーンアップ
# ============================================================================

echo ""
echo "【初期化】メモリをクリア..."
killall -9 ollama python3 llama-bench 2>/dev/null
sleep 2
sync
sudo sh -c 'echo 3 > /proc/sys/vm/drop_caches'
sleep 3

echo "✓ 初期化完了"
echo ""

# ============================================================================
# モデル 1: Bonsai-1.7B
# ============================================================================

echo "============================================================"
echo "  モデル 1: Bonsai-1.7B"
echo "============================================================"
cd "$BENCHMARK_DIR"

python3 benchmark_slm.py \
    --device "$DEVICE" \
    --models-filter "Bonsai" \
    2>&1 | tee "benchmark_bonsai_$(date +%Y%m%d_%H%M%S).log"

echo ""
echo "✓ Bonsai ベンチマーク完了"
echo ""
read -p "OS を再起動して次のモデルに進みます。Enter キーを押してください..." -n 1

sudo shutdown -r now

# ============================================================================
# モデル 2: LFM2.5
# ============================================================================
# （再起動後に実行）

echo ""
echo "============================================================"
echo "  モデル 2: LFM2.5"
echo "============================================================"
cd "$BENCHMARK_DIR"

python3 benchmark_slm.py \
    --device "$DEVICE" \
    --models-filter "LFM2.5" \
    2>&1 | tee "benchmark_lfm25_$(date +%Y%m%d_%H%M%S).log"

echo ""
echo "✓ LFM2.5 ベンチマーク完了"
echo ""
read -p "OS を再起動して次のモデルに進みます。Enter キーを押してください..." -n 1

sudo shutdown -r now

# ============================================================================
# モデル 3: Gemma 4 E2B
# ============================================================================
# （再起動後に実行）

echo ""
echo "============================================================"
echo "  モデル 3: Gemma 4 E2B"
echo "============================================================"
cd "$BENCHMARK_DIR"

python3 benchmark_slm.py \
    --device "$DEVICE" \
    --models-filter "Gemma 4 E2B" \
    2>&1 | tee "benchmark_gemma4e2b_$(date +%Y%m%d_%H%M%S).log"

echo ""
echo "============================================================"
echo "  すべてのベンチマーク完了！"
echo "============================================================"
echo ""
echo "結果ファイル："
ls -lh "$BENCHMARK_DIR"/benchmark_*.log
echo ""
